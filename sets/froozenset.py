# Creating an empty frozenset
empty_frozenset = frozenset()

# Creating a frozenset from a list
my_list = [1, 2, 3, 3]
my_frozenset = frozenset(my_list)

print(my_frozenset)

#In Python, a frozenset is an immutable version of a set. This means that once a frozenset is created, its elements cannot be changed.  